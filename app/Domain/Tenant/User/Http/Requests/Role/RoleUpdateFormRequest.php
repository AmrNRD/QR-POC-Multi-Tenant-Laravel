<?php

namespace App\Domain\Tenant\User\Http\Requests\Role;
use App\Domain\Tenant\User\Http\Requests\Role\RoleStoreFormRequest;

class RoleUpdateFormRequest extends RoleStoreFormRequest
{
    /**
     * Determine if the role is authorized to make this request.
     *
     * @return bool
     */
    public function authorize()
    {
        return true;
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules()
    {
        $rules = [
        // 'email'    => ['required','unique:roles,name,'.$this->route()->parameter('role').',id'],
        ];

        return array_merge(parent::rules(), $rules);
    }

    /**
     * Get custom attributes for validator errors.
     *
     * @return array
     */
    public function attributes()
    {
        return parent::attributes();
    }
}
